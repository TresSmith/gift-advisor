class SearchInfo {
    constructor(age, interest, ocassion) {
        this.age = age;
        this.interest = interest;
        this.ocassion = ocassion;
    }
    getAge() {
        return this.age;
    }
    getInterest() {
        return this.interest;
    }
    getOcassion() {
        return this.ocassion;
    }
}